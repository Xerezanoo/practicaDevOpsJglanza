"""Módulo para realizar operaciones matemáticas simples."""

def suma(a: int, b: int) -> int:
    """
    Calcula la suma de dos números enteros.

    Args:
        a: El primer número.
        b: El segundo número.

    Returns:
        La suma de los dos parámetros.
    """
    return a + b
